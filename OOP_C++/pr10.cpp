//Operators in C++ programming 
#include <iostream>
using namespace std;
int main(){
    int a,b;
    a=4,b=5;
    cout << " Operators in C++:"<< endl;
    cout << "Following are the types of the operators in C++"<< endl;
    //Arithmeitc operators
    cout << "The value of a+b is"<< a+b << endl;
    cout << "The value of a-b is"<< a-b << endl;
    cout << "The value of a*b is"<< a*b << endl;
    cout << "The value of a/b is"<< a/b << endl;
    cout << "The value of a%b is"<< a%b << endl;
    cout << "The value of a++ is"<< a++ << endl;
    cout << "The value of a-- is"<< a-- << endl;
    cout << "The value of ++a is"<< ++a << endl;
    cout << "The value of --a is"<< --a << endl;
    cout << endl;
    cout << endl;
    // // Assignment opertators
    // int a=3,b=9;
    // char d='d';
    // // Used to assign value to operators
    //Comparision Operators
    cout << " Following are the types of Comparision Operators in C++";
    cout << " The value of a == b is "<< (a==b) <<endl;
    cout << " The value of a != b is "<< (a!=b) <<endl;
    cout << " The value of a >= b is "<< (a>=b) <<endl;
    cout << " The value of a <= b is "<< (a<=b) <<endl;

    // Logical Operators
    // && || | 


    return 0;

}